let move = 'up';

switch (move) {
    case 'up':
        console.log('ke atas')
        break;
    case 'right':
        console.log('ke kanan')
        break;
    case 'left':
        console.log('ke kiri')
        break;
    case 'down':
        console.log('ke bawah')
    default:
        break;
}